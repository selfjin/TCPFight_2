#pragma comment(lib, "ws2_32.lib")
#pragma comment(lib, "winmm.lib")
#include <iostream>
#include "Network.h"
#include "Game.h"

//-------------------------------------------------------------
//  define 및 전역 변수
//-------------------------------------------------------------

// 1. 서버 ON/OFF

bool g_Shutdown = false;



int main()
{
	//---------------------------------------------------------------------/
	//							 초기화 함수
	//--------------------------------------------------------------------/
	timeBeginPeriod(1);

    //-------------------------------------------------------------
    // 네트워크 초기화 함수
    //-------------------------------------------------------------

	int startUp = WSASet();

	if (startUp == 0)
	{
		std::cout << " startUp Fail " << '\n';
	}

	std::cout << " startUp Success " << '\n';

	// ---------------------------------------------------------------------/
	//							     Main
	// --------------------------------------------------------------------/
	while (!g_Shutdown)
	{

		netIOProcess_RECV();

		Update();

		netIOProcess_SEND();

		///////////////////////////////////////////////////////////////////
		netSession();

		Sleep(20);
	}


	




	return 0;
}



